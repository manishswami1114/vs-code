// C++ program to illustrate do-while loop
 
#include <iostream>
using namespace std;
 
int main()
{
    // Initialization expression
    int i = 5;
 
    do {
        // Loop body
        cout << "Hello World\n";
 
        // Update expression
        i++;
 
    }
    // Test expression
    while (i < 1);
 
    return 0;
}